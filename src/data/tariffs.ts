// Enrich tariff with operator and region details
export const enrichTariffWithDetails = (tariff: Tariff): TariffWithDetails => {
  const operator = operators.find(op => op.id === tariff.operatorId)!;
  const tariffRegions = regions.filter(r => tariff.regionIds.includes(r.id));

  return {
    ...tariff,
    operator,
    regions: tariffRegions,
  };
};

// Get all tariffs with details
export const getAllTariffsWithDetails = (): TariffWithDetails[] => {
  return tariffs.map(enrichTariffWithDetails);
};

// Filter tariffs based on search params
export const filterTariffs = (params: SearchParams): TariffWithDetails[] => {
  let results = [...tariffs];

  // Filter by region
  if (params.regionId) {
    results = results.filter(t => t.regionIds.includes(params.regionId!));
  }

  // Filter by max price
  if (params.maxPrice) {
    results = results.filter(t => t.price <= params.maxPrice!);
  }

  // Filter by internet
  if (params.internetUnlimited) {
    results = results.filter(t => t.internetUnlimited);
  } else if (params.minInternetGb) {
    results = results.filter(t => 
      t.internetUnlimited || (t.internetGb && t.internetGb >= params.minInternetGb!)
    );
  }

  // Filter by minutes
  if (params.minutesUnlimited) {
    results = results.filter(t => t.minutesUnlimited);
  } else if (params.minMinutes) {
    results = results.filter(t => 
      t.minutesUnlimited || (t.minutes && t.minutes >= params.minMinutes!)
    );
  }

  // Filter by SMS
  if (params.smsUnlimited) {
    results = results.filter(t => t.smsUnlimited);
  } else if (params.minSms) {
    results.filter(t => 
      t.smsUnlimited || (t.sms && t.sms >= params.minSms!)
    );
  }

  // Filter by options
  if (params.unlimitedSocial) {
    results = results.filter(t => t.unlimitedSocial);
  }
  if (params.unlimitedMessengers) {
    results = results.filter(t => t.unlimitedMessengers);
  }
  if (params.hotspot) {
    results = results.filter(t => t.hotspot);
  }
  if (params.esim) {
    results = results.filter(t => t.esim);
  }
  if (params.familyTariff) {
    results = results.filter(t => t.familyTariff);
  }

  // Filter by operators
  if (params.operatorIds && params.operatorIds.length > 0) {
    results = results.filter(t => params.operatorIds!.includes(t.operatorId));
  }

  // Sort by price
  results.sort((a, b) => a.price - b.price);

  return results.map(enrichTariffWithDetails);
};

// Get best tariff for region
export const getBestTariffForRegion = (regionId: string): TariffWithDetails | undefined => {
  const regionTariffs = tariffs.filter(t => 
    t.isActive && t.regionIds.includes(regionId)
  );

  if (regionTariffs.length === 0) return undefined;

  const scored = regionTariffs.map(t => {
    const internetValue = t.internetUnlimited ? 1000 : (t.internetGb || 0);
    const minutesValue = t.minutesUnlimited ? 1000 : (t.minutes || 0);
    const score = (internetValue + minutesValue / 10) / (t.price || 1);
    return { tariff: t, score };
  });

  scored.sort((a, b) => b.score - a.score);

  return enrichTariffWithDetails(scored[0].tariff);
};

// Get top tariffs for region
export const getTopTariffsForRegion = (regionId: string, limit: number = 3): TariffWithDetails[] => {
  const regionTariffs = tariffs.filter(t => 
    t.isActive && t.regionIds.includes(regionId)
  );

  const scored = regionTariffs.map(t => {
    const internetValue = t.internetUnlimited ? 1000 : (t.internetGb || 0);
    const minutesValue = t.minutesUnlimited ? 1000 : (t.minutes || 0);
    const score = (internetValue + minutesValue / 10) / (t.price || 1);
    return { tariff: t, score };
  });

  scored.sort((a, b) => b.score - a.score);

  return scored.slice(0, limit).map(s => enrichTariffWithDetails(s.tariff));
};

// Get tariff by ID
export const getTariffById = (id: string): TariffWithDetails | undefined => {
  const tariff = tariffs.find(t => t.id === id);
  return tariff ? enrichTariffWithDetails(tariff) : undefined;
};

